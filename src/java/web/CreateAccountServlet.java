    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
     
       String userId = request.getParameter("userId");
       String balance = request.getParameter("balance");
       //capturando los datos que entra el usuario 
       if(userId == null || balance == null){
                RequestDispatcher requestDispatcher = request.getRequestDispatcher("createAccountForm.jsp");
                requestDispatcher.forward(request, response);
            }else{
                Account account = new Account(Long.parseLong(userId), Double.parseDouble(balance));
                    AccountServiceImpl service = new AccountServiceImpl();
                    boolean result = service.createAccount(account);
                    response.sendRedirect("showCreateAccount.jsp?resultado=" + result);
                    }
    }

